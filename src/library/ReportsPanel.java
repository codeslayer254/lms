   /**
     * The method to query all the Transaction History and populate all the History on the table
     */
    public static void allHistory(){
        String sql = "SELECT * FROM TRANSACTION_HISTORY ";
        
        try {
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            historyTable.setModel(DbUtils.resultSetToTableModel(rs));
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }

