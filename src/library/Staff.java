    /**
     * This method is responsible for adding staff into the database We re-use
     * the object of users for getting username and password from inputs The
     * dialog is shown to user for confirmation
     *
     * @param evt
     */
    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed

        StaffDao staffDao = new StaffDao();
        Users user = new Users();

        if (fieldsAreValid()) {
            
            user.setUsername(usernameTxt.getText());
            user.setPassword(passTxt.getText());
            user.setFirstName(FnameTF.getText());
            user.setLastName(LnameTF.getText());
            user.setEmail(EmailTF.getText());
            user.setRole("ADMIN");

            int reply = JOptionPane.showConfirmDialog(null, "Confirm Adding Staff: \n" + usernameTxt.getText());
            if (reply == JOptionPane.YES_OPTION) {
                try {
                    staffDao.save(user);
                    JOptionPane.showMessageDialog(null, "Staff added Successfully");
                } catch (Exception ex) {
                    Logger.getLogger(Staff.class.getName()).log(Level.SEVERE, null, ex);
                }

            } else {
                JOptionPane.showMessageDialog(null, "You cancelled additing staff");
            }
            allStaff();

        } else {
            JOptionPane.showMessageDialog(null, "Please fill in missing fields", "Missing Fields", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_saveBtnActionPerformed

    private boolean fieldsAreValid() {

        if (usernameTxt.getText().equals(null) || usernameTxt.getText().equals("")
                || passTxt.getText().equals(null) || passTxt.getText().equals("")
                || FnameTF.getText().equals(null) || FnameTF.getText().equals("")
                || LnameTF.getText().equals("") || EmailTF.getText().equals("")) {
            return false;
        } else {
            return true;
        }

    }
    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed

        StaffDao staffDao = new StaffDao();
        Users user = new Users();

        user.setId(currentStaff.getId());
        user.setUsername(usernameTxt.getText());
        user.setPassword(passTxt.getText());
        user.setFirstName(FnameTF.getText());
        user.setLastName(LnameTF.getText());
        user.setEmail(EmailTF.getText());

        int reply = JOptionPane.showConfirmDialog(null, "Confirm Updating Staff: \n" + usernameTxt.getText());
        if (reply == JOptionPane.YES_OPTION) {
            try {
                staffDao.update(user);
                JOptionPane.showMessageDialog(null, "Staff updated Successfully");

                //Clear the input fields
                usernameTxt.setText("");
                passTxt.setText("");
                FnameTF.setText("");
                LnameTF.setText("");
                EmailTF.setText("");

            } catch (Exception ex) {
                Logger.getLogger(Staff.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {
            JOptionPane.showMessageDialog(null, "You cancelled updating staff");
        }
        allStaff();

    }//GEN-LAST:event_updateBtnActionPerformed

