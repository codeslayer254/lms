    private void btnGuestLoginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnGuestLoginMouseClicked

        //Redirect to Guest Panel
        
        if(usernameTxt.equals(null) || usernameTxt.equals("")){
            
             JOptionPane.showMessageDialog (null, "Please eneter your user name to countinue as Guest", "Gues Username required", JOptionPane.ERROR_MESSAGE);
                usernameTxt.setText("");
                usernameTxt.requestFocus(true);
        }
        else {
            
            libUser = new Users();
            libUser.setUsername(usernameTxt.getText());
            GuestPanel panel = new GuestPanel();
            panel.setVisible(true);
            dispose();
        }
        
    }//GEN-LAST:event_btnGuestLoginMouseClicked

    private void btnLoginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnLoginMouseClicked
        // TODO add your handling code here:
        
        try {
            
            UserDao userDao = new UserDao();
            libUser = userDao.loginUser(usernameTxt.getText(), passwordTxt.getText());
            
            if(libUser!=null){
                
                if(libUser.getRole().equalsIgnoreCase("librarian")){
                 
                //Redirect to Librarian Panel
                 LibrarianPanel panel = new LibrarianPanel();
                 panel.setVisible(true);
                 dispose();
                }
                
                if(libUser.getRole().equalsIgnoreCase("admin")){
                 
                //Redirect to Admin Panel
                 AdminPanel panel = new AdminPanel();
                 panel.setVisible(true);
                 dispose();
                }
                
                
                 
            }
            else
            {
                
                JOptionPane op = new JOptionPane("Wrong username or password! Please try again.", JOptionPane.ERROR_MESSAGE);
                JDialog dialog = op.createDialog("Login Error");
                dialog.setAlwaysOnTop(true); 
                dialog.setModal(true);
                dialog.setDefaultCloseOperation(JDialog.DISPOSE_ON_CLOSE);
                dialog.setVisible(true);
                usernameTxt.setText("");
                passwordTxt.setText("");
                usernameTxt.requestFocus(true);
   
            }
        
            
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, "Failed to login " + e);
        }
    }//GEN-LAST:event_btnLoginMouseClicked

    public boolean isLoginValid(String username, String password) throws RemoteException {
        try {
            UserDao userDao = new UserDao();
            List<Users> listOfUsers = userDao.getAllUsers();
            for (Users user: listOfUsers) {
                if (user.getUsername().equalsIgnoreCase(username)) {
                    if (user.getPassword().equalsIgnoreCase(password)) {
                        
                        return true;
                    }
                }
                }
        } catch (Exception ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }   
       return false;
    }
    
